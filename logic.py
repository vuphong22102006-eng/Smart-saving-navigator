import math 
from abc import ABC, abstractmethod
from datetime import datetime
from typing import List


class FinancialGoal(ABC):
    """
    Abstract Base Class for all financial goals.
    This class defines the shared structure and behaviors
    of every financial goal in the Smart Savings Navigator system.
    """

    def __init__(
        self,
        goal_id: str,
        goal_name: str,
        target: float,
        start_date: str,
        deadline: str,
        annual_interest_rate: float = 0.0,
        monthly_contribution: float = 0.0,
        goal_type: str = "Generic",
        currency = "VND"
    ):
        self._goal_id = goal_id
        self._goal_name = goal_name
        self._target = target
        self._currency = currency 
        self._balance = 0.0
        self._start_date = datetime.strptime(start_date, "%Y-%m-%d")
        self._deadline = datetime.strptime(deadline, "%Y-%m-%d")
        self._annual_interest_rate = annual_interest_rate
        self._monthly_contribution = monthly_contribution
        self._goal_type = goal_type
        self._status = "In Progress"

    # =========================
    # Getter Methods
    # =========================

    def get_goal_id(self) -> str:
        return self._goal_id

    def get_goal_name(self) -> str:
        return self._goal_name

    def get_target(self) -> float:
        return self._target

    def get_balance(self) -> float:
        return self._balance

    def get_status(self) -> str:
        return self._status

    # =========================
    # Core Financial Actions
    # =========================

    def deposit(self, amount: float):
        if amount <= 0:
            raise ValueError("Deposit amount must be positive.")
        self._balance += amount
        self._update_status()

    def withdraw(self, amount: float):
        if amount <= 0:
            raise ValueError("Withdraw amount must be positive.")
        if amount > self._balance:
            raise ValueError("Insufficient balance.")
        self._balance -= amount
        self._update_status()

    # =========================
    # Progress & Calculation
    # =========================

    def get_progress_percentage(self) -> float:
        if self._target == 0:
            return 0
        return (self._balance / self._target) * 100

    def get_remaining_amount(self) -> float:
        return max(self._target - self._balance, 0)

    def get_remaining_days(self) -> int:
        today = datetime.today()
        return (self._deadline - today).days
    def get_remaining_months(self) -> float:
        return math.ceil(self.get_remaining_days() / 30)

    def _update_status(self):
        if self._balance >= self._target:
            self._status = "Completed"
        else:
            self._status = "In Progress"

    # =========================
    # Abstract Method
    # =========================

    @abstractmethod
    def recalculate_route(self):
        """
        This method must be implemented by subclasses.
        It recalculates the saving plan when user deviates
        from the original contribution plan.
        """
        pass

    # =========================
    # Utility
    # =========================

    def to_dict(self) -> dict:
        return {
            "goal_id": self._goal_id,
            "goal_name": self._goal_name,
            "target": self._target,
            "currency": self._currency,
            "balance": self._balance,
            "start_date": self._start_date.strftime("%Y-%m-%d"),
            "deadline": self._deadline.strftime("%Y-%m-%d"),
            "annual_interest_rate": self._annual_interest_rate,
            "monthly_contribution": self._monthly_contribution,
            "goal_type": self._goal_type,
            "status": self._status
        }


class GoalManager:
    """
    Manages multiple FinancialGoal objects.
    """

    def __init__(self):
        self._goals: List[FinancialGoal] = []

    def add_goal(self, goal: FinancialGoal):
        self._goals.append(goal)

    def remove_goal(self, goal_id: str):
        self._goals = [
            goal for goal in self._goals
            if goal.get_goal_id() != goal_id
        ]

    def find_goal_by_id(self, goal_id: str) -> FinancialGoal:
        for goal in self._goals:
            if goal.get_goal_id() == goal_id:
                return goal
        raise ValueError("Goal not found.")

    def list_goals(self) -> List[FinancialGoal]:
        return self._goals

    def deposit_to_goal(self, goal_id: str, amount: float):
        goal = self.find_goal_by_id(goal_id)
        goal.deposit(amount)

    def withdraw_from_goal(self, goal_id: str, amount: float):
        goal = self.find_goal_by_id(goal_id)
        goal.withdraw(amount)

    def to_list(self) -> List[dict]:
        return [goal.to_dict() for goal in self._goals]

