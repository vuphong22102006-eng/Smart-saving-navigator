class DatabaseManager:
    def __init__(self, db_name="smart_savings.db"):
        self.db_name = db_name
        self._create_tables()

    def _get_connection(self):
        return sqlite3.connect(self.db_name)
    def delete_goal(self, goal_id: str):
        """Xoá mục tiêu và toàn bộ lịch sử giao dịch liên quan."""
        with self._get_connection() as conn:
            # Xoá lịch sử giao dịch trước để tránh lỗi ràng buộc dữ liệu
            conn.execute("DELETE FROM TransactionHistory WHERE goal_id = ?", (goal_id,))
            # Sau đó xoá mục tiêu
            conn.execute("DELETE FROM Goals WHERE goal_id = ?", (goal_id,))
            conn.commit()

    def _create_tables(self):
        query_goals = """
        CREATE TABLE IF NOT EXISTS Goals (
            goal_id TEXT PRIMARY KEY,
            goal_name TEXT NOT NULL,
            target REAL NOT NULL,
            currency TEXT DEFAULT 'VND',
            balance REAL DEFAULT 0.0,
            start_date TEXT NOT NULL,
            deadline TEXT NOT NULL,
            annual_interest_rate REAL DEFAULT 0.0,
            monthly_contribution REAL DEFAULT 0.0,
            goal_type TEXT DEFAULT 'Generic',
            status TEXT DEFAULT 'In Progress'
        );
        """

        query_history = """
        CREATE TABLE IF NOT EXISTS TransactionHistory (
            transaction_id INTEGER PRIMARY KEY AUTOINCREMENT,
            goal_id TEXT NOT NULL,
            amount REAL NOT NULL,
            transaction_type TEXT CHECK(transaction_type IN ('DEPOSIT', 'WITHDRAW')),
            timestamp TEXT NOT NULL,
            FOREIGN KEY (goal_id) REFERENCES Goals (goal_id)
        );
        """

        # Trigger tự động cập nhật số dư khi có giao dịch (Encapsulation ở mức DB)
        trigger_update_balance = """
        CREATE TRIGGER IF NOT EXISTS update_goal_balance
        AFTER INSERT ON TransactionHistory
        BEGIN
            UPDATE Goals
            SET balance = balance + NEW.amount,
                status = CASE
                    WHEN (balance + NEW.amount) >= target THEN 'Completed'
                    ELSE 'In Progress'
                END
            WHERE goal_id = NEW.goal_id;
        END;
        """

        with self._get_connection() as conn:
            cursor = conn.cursor()
            cursor.execute(query_goals)
            cursor.execute(query_history)
            cursor.execute(trigger_update_balance)
            conn.commit()

    # =========================
    # Methods cho Goals
    # =========================

    def save_goal(self, goal_dict: dict):
        """Lưu một đối tượng FinancialGoal (dạng dict) vào DB."""
        query = """
        INSERT OR REPLACE INTO Goals
        (goal_id, goal_name, target, currency, balance, start_date, deadline, annual_interest_rate, monthly_contribution, goal_type, status)
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """
        data = (
            goal_dict['goal_id'], goal_dict['goal_name'], goal_dict['target'],
            goal_dict['currency'],
            goal_dict['balance'], goal_dict['start_date'], goal_dict['deadline'],
            goal_dict['annual_interest_rate'], goal_dict['monthly_contribution'],
            goal_dict['goal_type'], goal_dict['status']
        )
        with self._get_connection() as conn:
            conn.execute(query, data)
            conn.commit()

    # =========================
    # Methods cho Transactions
    # =========================

    def add_transaction(self, goal_id: str, amount: float, t_type: str):
        """
        Thực hiện nạp/rút tiền.
        Lưu ý: Không update trực tiếp bảng Goals, Trigger sẽ lo việc đó.
        """
        if t_type == 'WITHDRAW':
            # Kiểm tra số dư trước khi rút (Logic nghiệp vụ)
            current_balance = self.get_goal_balance(goal_id)
            if amount > current_balance:
                raise ValueError("Insufficient balance in database.")
            actual_amount = -amount
        else:
            actual_amount = amount

        query = """
        INSERT INTO TransactionHistory (goal_id, amount, transaction_type, timestamp)
        VALUES (?, ?, ?, ?)
        """
        with self._get_connection() as conn:
            conn.execute(query, (goal_id, actual_amount, t_type, datetime.now().isoformat()))

    def get_goal_balance(self, goal_id: str) -> float:
        query = "SELECT balance FROM Goals WHERE goal_id = ?"
        with self._get_connection() as conn:
            res = conn.execute(query, (goal_id,)).fetchone()
            return res[0] if res else 0.0

    def get_history(self, goal_id: str):
        query = "SELECT * FROM TransactionHistory WHERE goal_id = ? ORDER BY timestamp DESC"
        with self._get_connection() as conn:
            return conn.execute(query, (goal_id,)).fetchall()

